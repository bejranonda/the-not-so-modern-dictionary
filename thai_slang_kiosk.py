    def save_data(self):
        json_file = "output/user_added_slang.json"
        now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        word = self.data.get("word")
        meaning = self.data.get("meaning")
        example = self.data.get("example")
        author = self.data.get("author", None)  # р╕нр╕▓р╕Ир╣Др╕бр╣Ир╕бр╕╡ author р╣Гр╕Щр╕гр╕нр╕Ър╕Бр╣Ир╕нр╕Щр╕лр╕Щр╣Йр╕▓

        # р╣Вр╕лр╕ер╕Фр╕Вр╣Йр╕нр╕бр╕╣р╕ер╣Ар╕Бр╣Ир╕▓
        if os.path.exists(json_file):
            with open(json_file, "r", encoding="utf-8") as f:
                slang_data = json.load(f)
        else:
            slang_data = {}

        if word not in slang_data:
            slang_data[word] = {
                "meaning": [meaning],
                "example": [example],
                "reach": 1,
                "update": now,
                "author": [author] if author else []
            }
        else:
            entry = slang_data[word]
            # р╣Ар╕Юр╕┤р╣Ир╕бр╕Др╕зр╕▓р╕бр╕лр╕бр╕▓р╕в р╕Цр╣Йр╕▓р╕вр╕▒р╕Зр╣Др╕бр╣Ир╕бр╕╡
            if meaning not in entry["meaning"]:
                entry["meaning"].append(meaning)
            # р╣Ар╕Юр╕┤р╣Ир╕бр╕Хр╕▒р╕зр╕нр╕вр╣Ир╕▓р╕З р╕Цр╣Йр╕▓р╕вр╕▒р╕Зр╣Др╕бр╣Ир╕бр╕╡
            if example not in entry["example"]:
                entry["example"].append(example)
            # р╣Ар╕Юр╕┤р╣Ир╕б reach
            entry["reach"] = entry.get("reach", 0) + 1
            # р╕нр╕▒р╕Юр╣Ар╕Фр╕Хр╣Ар╕зр╕ер╕▓
            entry["update"] = now
            
            # р╕Ир╕▒р╕Фр╕Бр╕▓р╕г author
            if author:
                authors = entry.get("author", [])
                if author in authors:
                    authors.remove(author)
                authors.append(author)  # р╕вр╣Йр╕▓р╕вр╣Др╕Ыр╕Чр╣Йр╕▓р╕вр╣Ар╕кр╕бр╕н
                entry["author"] = authors

        # р╕Ър╕▒р╕Щр╕Чр╕╢р╕Бр╕Бр╕ер╕▒р╕Ър╣Др╕Яр╕ер╣М JSON
        with open(json_file, "w", encoding="utf-8") as f:
            json.dump(slang_data, f, ensure_ascii=False, indent=4)
            print(f"тЬЕ р╕Ър╕▒р╕Щр╕Чр╕╢р╕Бр╕ер╕З JSON  {json_file}")


